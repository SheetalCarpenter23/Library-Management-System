    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
     String memberName = txtmembername.getText();
    BookItem bitem = (BookItem) txtbook.getSelectedItem();

    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String issueDate = sdf.format(txtissue.getDate());
    String returnDate = sdf.format(txtreturn.getDate());


  
    try {
        pat = con.prepareStatement(
            "INSERT INTO issuebook(membername, bookid, issuedate, returndate) VALUES (?,?,?,?)",
        Statement.RETURN_GENERATED_KEYS      
        );

      
        pat.setString(1,memberName);
        pat.setInt(2, bitem.id);
        pat.setString(3, issueDate);
        pat.setString(4, returnDate);

        int k = pat.executeUpdate();
        if(k == 1) {
            rs=pat.getGeneratedKeys();
            if(rs.next())
            {
                int generatedId = rs.getInt(1);
                txtid.setText(String.valueOf(generatedId));
            }
            JOptionPane.showMessageDialog(this, "Book Issued!");
           
            IssueBookLoad(); 
            table_update();// refresh JTable
            clearForm();       // clear form
            
        } else {
            JOptionPane.showMessageDialog(this, "Error adding record.");
        }
    } catch(SQLException ex) {
        Logger.getLogger(Issuebook.class.getName()).log(Level.SEVERE, null, ex);
  }
 
    }//GEN-LAST:event_jButton1ActionPerformed

