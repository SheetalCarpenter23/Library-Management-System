    public void table_update() {
    int c;
    try {
       pat = con.prepareStatement(
            "SELECT i.memberid, i.membername, b.bname, i.issuedate, i.returndate " +
            "FROM issuebook i JOIN book b ON i.bookid = b.id"
        );
        rs = pat.executeQuery();
        ResultSetMetaData rsd = rs.getMetaData();
        c = rsd.getColumnCount();
        
        DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
        d.setRowCount(0);   // clear old rows
        
        while (rs.next()) {
            Vector v2 = new Vector();
            for (int i = 1; i <= c; i++) {
                v2.add(rs.getString(i));
            }
            d.addRow(v2);    // add each row to JTable
        }
    } catch (SQLException ex) {
        Logger.getLogger(Issuebook.class.getName()).log(Level.SEVERE, null, ex);
    }
}

  
