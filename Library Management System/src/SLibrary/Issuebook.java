    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
 DefaultTableModel d1 = (DefaultTableModel) jTable1.getModel();
    int selectIndex = jTable1.getSelectedRow();

    if(selectIndex == -1) {
        JOptionPane.showMessageDialog(this, "Select a record to update.");
        return;
    }

    int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
    String memberName = txtmembername.getText();
    BookItem bitem = (BookItem) txtbook.getSelectedItem();

    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String issueDate = sdf.format(txtissue.getDate());
    String returnDate = sdf.format(txtreturn.getDate());

    try {
        pat = con.prepareStatement(
            "UPDATE issuebook SET membername=?, bookid=?, issuedate=?, returndate=? WHERE memberid=?"
        );
        pat.setString(1, memberName);
        pat.setInt(2, bitem.id);
        pat.setString(3, issueDate);
        pat.setString(4, returnDate);
        pat.setInt(5, id);

        int k = pat.executeUpdate();
        if(k == 1) {
            JOptionPane.showMessageDialog(this, "Record Updated!");
            IssueBookLoad();
            clearForm();
            jButton1.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Update Failed!");
        }
    } catch(SQLException ex) {
        Logger.getLogger(Issuebook.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

 DefaultTableModel d1 = (DefaultTableModel) jTable1.getModel();
    int selectIndex = jTable1.getSelectedRow();

    if (selectIndex == -1) {
        JOptionPane.showMessageDialog(this, "Please select a record to delete.");
        return;
    }

    int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

    int dialogResult = JOptionPane.showConfirmDialog(
        this,
        "Do you really want to delete this record?",
        "Warning",
        JOptionPane.YES_NO_OPTION
    );

    if (dialogResult == JOptionPane.YES_OPTION) {
        try {
            pat = con.prepareStatement("DELETE FROM issuebook WHERE memberid=?");
            pat.setInt(1, id);

            int k = pat.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
                IssueBookLoad();      // Refresh table after delete
                clearForm();
                jButton1.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Delete Failed!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Issuebook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
   
    }//GEN-LAST:event_jButton3ActionPerformed

